package com.main;

public class IfElse {

	public static void main(String[] args) {
		int val = 5001 ; 
		if(val <1000) {
			System.out.println("it is less than 1000");
		}
		else if (val <2000) {
			System.out.println("it is less than 2000");
		}
		else {
			System.out.println("none of the above");
		}
	}

}

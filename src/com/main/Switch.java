package com.main;

public class Switch {

	public static void main(String[] args) {
		int a = 2; 
		switch(a) {
		case 1 : 
			System.out.println("the value of a = 1");
			break;
		case 2 :
			System.out.println("the value of a = 2");
			break;
		case 3 :
			System.out.println("the value of b = 3 ");
			break;
		default : 
			System.out.println(" none of the above");
			
		}
	}

}
